import '../models/user_progress.dart';

class StreakService {

  //////////////////////////////////////////////////
  // UPDATE LOGIN STREAK
  //////////////////////////////////////////////////

  static UserProgress updateLoginStreak(
    UserProgress progress,
  ) {

    final now = DateTime.now();

    //////////////////////////////////////////////////
    // TODAY DATE ONLY
    //////////////////////////////////////////////////

    final today = DateTime(
      now.year,
      now.month,
      now.day,
    );

    //////////////////////////////////////////////////
    // FIRST LOGIN
    //////////////////////////////////////////////////

    if (progress.lastLogin.isEmpty) {

      progress.streak = 1;

      progress.lastLogin =
          today.toIso8601String();

      return progress;
    }

    //////////////////////////////////////////////////
    // LAST LOGIN DATE
    //////////////////////////////////////////////////

    final lastLoginDate =
        DateTime.parse(progress.lastLogin);

    final lastDay = DateTime(
      lastLoginDate.year,
      lastLoginDate.month,
      lastLoginDate.day,
    );

    //////////////////////////////////////////////////
    // DIFFERENCE
    //////////////////////////////////////////////////

    final difference =
        today.difference(lastDay).inDays;

    //////////////////////////////////////////////////
    // SAME DAY LOGIN
    //////////////////////////////////////////////////

    if (difference == 0) {

      return progress;
    }

    //////////////////////////////////////////////////
    // NEXT DAY LOGIN
    //////////////////////////////////////////////////

    if (difference == 1) {

      progress.streak += 1;
    }

    //////////////////////////////////////////////////
    // MISSED DAYS
    //////////////////////////////////////////////////

    else if (difference > 1) {

      progress.streak = 1;
    }

    //////////////////////////////////////////////////
    // UPDATE LAST LOGIN
    //////////////////////////////////////////////////

    progress.lastLogin =
        today.toIso8601String();

    return progress;
  }

  //////////////////////////////////////////////////
  // ADD XP
  //////////////////////////////////////////////////

  static UserProgress addXP(
    UserProgress progress,
    int xp,
  ) {

    progress.xp += xp;

    //////////////////////////////////////////////////
    // LEVEL UP
    //////////////////////////////////////////////////

    while (progress.xp >=
        progress.level * 100) {

      progress.xp -=
          progress.level * 100;

      progress.level += 1;

      //////////////////////////////////////////////////
      // BONUS STARS
      //////////////////////////////////////////////////

      progress.stars += 5;
    }

    return progress;
  }

  //////////////////////////////////////////////////
  // ADD STARS
  //////////////////////////////////////////////////

  static UserProgress addStars(
    UserProgress progress,
    int stars,
  ) {

    progress.stars += stars;

    return progress;
  }
}